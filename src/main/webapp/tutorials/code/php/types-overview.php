<?php
// PHP Data Types

// 1. String
$text = "Hello, World!";

// 2. Integer
$number = 42;

// 3. Float (Double)
$decimal = 3.14159;

// 4. Boolean
$isTrue = true;
$isFalse = false;

// 5. Array
$colors = ["red", "green", "blue"];

// 6. NULL
$empty = null;

// Display types using var_dump()
echo "String: ";
var_dump($text);
echo "\n";

echo "Integer: ";
var_dump($number);
echo "\n";

echo "Float: ";
var_dump($decimal);
echo "\n";

echo "Boolean: ";
var_dump($isTrue);
echo "\n";

echo "Array: ";
var_dump($colors);
echo "\n";

echo "NULL: ";
var_dump($empty);
