<?php
// Arithmetic Operators

$a = 10;
$b = 3;

// Addition
echo "Addition: " . ($a + $b);  // 13
echo "\n";

// Subtraction
echo "Subtraction: " . ($a - $b);  // 7
echo "\n";

// Multiplication
echo "Multiplication: " . ($a * $b);  // 30
echo "\n";

// Division
echo "Division: " . ($a / $b);  // 3.333...
echo "\n";

// Modulus (remainder)
echo "Modulus: " . ($a % $b);  // 1
echo "\n";

// Exponentiation (PHP 5.6+)
echo "Exponentiation: " . ($a ** $b);  // 1000
echo "\n";

// Negation
echo "Negation: " . (-$a);  // -10
