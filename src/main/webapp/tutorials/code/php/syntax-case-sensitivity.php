<?php
// PHP is case-sensitive for variables
$name = "John";
$Name = "Jane";  // Different variable!

echo $name;  // Outputs: John
echo "\n";
echo $Name;  // Outputs: Jane

// But case-INsensitive for functions and keywords
echo "This works!";  // ECHO is same as echo
echo "This too!";    // Echo is same as echo
