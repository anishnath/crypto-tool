# Variable Assignment in Python
# No declaration needed - just assign a value

# Simple assignment
name = "Alice"
age = 25
price = 19.99
is_active = True

# Print the variables
print("Name:", name)
print("Age:", age)
print("Price:", price)
print("Is Active:", is_active)

# Check types
print("\nTypes:")
print("name is", type(name))
print("age is", type(age))
print("price is", type(price))
print("is_active is", type(is_active))
