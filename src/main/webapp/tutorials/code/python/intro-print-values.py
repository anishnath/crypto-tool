# Printing different types of values

# Print text (strings)
print("Hello, Python!")

# Print numbers
print(42)
print(3.14159)

# Print calculations
print(10 + 5)
print(100 / 4)

# Print multiple values (separated by commas)
print("The answer is", 42)
print("Sum:", 10 + 20 + 30)
print("Name:", "Alice", "Age:", 25)
