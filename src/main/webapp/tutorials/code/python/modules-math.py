# Python Math
# Python has a set of built-in math functions, including an extensive math module.

# 1. Built-in Math Functions
x = min(5, 10, 25)
y = max(5, 10, 25)
print(f"Min: {x}, Max: {y}")

z = abs(-7.25)
print(f"Abs: {z}")

p = pow(4, 3) # 4 to the power of 3
print(f"Pow(4,3): {p}")

# 2. The Math Module
import math

root = math.sqrt(64)
print(f"Square root of 64: {root}")

ceil_val = math.ceil(1.4)
floor_val = math.floor(1.4)
print(f"Ceil: {ceil_val}, Floor: {floor_val}")

print(f"Pi: {math.pi}")
