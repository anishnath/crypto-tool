# Comparison Operators in Python
# These operators compare two values and return a boolean (True or False)

x = 10
y = 20

print(f"x = {x}, y = {y}")

# 1. Equal to (==)
print(f"x == y: {x == y}")

# 2. Not equal to (!=)
print(f"x != y: {x != y}")

# 3. Greater than (>)
print(f"x > y: {x > y}")

# 4. Less than (<)
print(f"x < y: {x < y}")

# 5. Greater than or equal to (>=)
print(f"x >= 10: {x >= 10}")

# 6. Less than or equal to (<=)
print(f"y <= 20: {y <= 20}")

# Chained Comparison
z = 15
print(f"10 < {z} < 20: {10 < z < 20}")  # Equivalent to: 10 < z and z < 20
