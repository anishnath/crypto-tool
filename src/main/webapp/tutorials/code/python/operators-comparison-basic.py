# Basic Comparison Operators

a = 10
b = 5
c = 10

# Equal to (==)
print("a == b:", a == b)   # False
print("a == c:", a == c)   # True

# Not equal to (!=)
print("a != b:", a != b)   # True
print("a != c:", a != c)   # False

print()

# Greater than (>)
print("a > b:", a > b)     # True
print("a > c:", a > c)     # False

# Less than (<)
print("a < b:", a < b)     # False
print("b < a:", b < a)     # True

print()

# Greater than or equal (>=)
print("a >= b:", a >= b)   # True
print("a >= c:", a >= c)   # True (equal counts!)

# Less than or equal (<=)
print("a <= b:", a <= b)   # False
print("a <= c:", a <= c)   # True (equal counts!)
