-- Both single and double quotes work
print("Double quotes")
print('Single quotes')

-- They're interchangeable
print("It's easy to use apostrophes with double quotes")
print('You can use "quotes" inside single quotes')

-- Escape sequences
print("Line 1\nLine 2")  -- \n creates a new line
print("Tab\there")        -- \t creates a tab

-- Multi-line strings
print([[This is a
multi-line string
using double brackets]])
