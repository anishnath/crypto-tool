-- Variables in Lua are easy!
-- Use 'local' to create a local variable (recommended)
local name = "Lua"
local version = 5.4
local isAwesome = true

-- Print the variables
print("Language:", name)
print("Version:", version)
print("Is awesome?", isAwesome)

-- Variables can change their value
local count = 10
print("Count:", count)

count = 20  -- Change the value
print("New count:", count)

-- You can assign multiple variables at once
local x, y, z = 1, 2, 3
print("x =", x, "y =", y, "z =", z)
