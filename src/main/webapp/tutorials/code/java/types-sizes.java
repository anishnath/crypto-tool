public class Main {
    public static void main(String[] args) {
        System.out.println("--- Wrapper Classes MIN/MAX Values ---");

        // Byte
        System.out.println("Byte Min: " + Byte.MIN_VALUE);
        System.out.println("Byte Max: " + Byte.MAX_VALUE);
        System.out.println("Byte Size: " + Byte.SIZE + " bits");

        // Short
        System.out.println("\nShort Min: " + Short.MIN_VALUE);
        System.out.println("Short Max: " + Short.MAX_VALUE);
        System.out.println("Short Size: " + Short.SIZE + " bits");

        // Integer (int)
        System.out.println("\nInteger Min: " + Integer.MIN_VALUE);
        System.out.println("Integer Max: " + Integer.MAX_VALUE);
        System.out.println("Integer Size: " + Integer.SIZE + " bits");

        // Long
        System.out.println("\nLong Min: " + Long.MIN_VALUE);
        System.out.println("Long Max: " + Long.MAX_VALUE);
        System.out.println("Long Size: " + Long.SIZE + " bits");

        // Float
        System.out.println("\nFloat Min: " + Float.MIN_VALUE);
        System.out.println("Float Max: " + Float.MAX_VALUE);

        // Double
        System.out.println("\nDouble Min: " + Double.MIN_VALUE);
        System.out.println("Double Max: " + Double.MAX_VALUE);
    }
}
