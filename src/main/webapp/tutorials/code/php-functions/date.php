<?php
// Current date and time
echo date("Y-m-d");        // 2025-01-28
echo "\n";
echo date("H:i:s");        // 14:30:45
echo "\n";
echo date("Y-m-d H:i:s");  // Full datetime
echo "\n";

// Common formats
echo date("F j, Y");       // January 28, 2025
echo "\n";
echo date("l");            // Tuesday (day name)
echo "\n";
echo date("D, M d");       // Tue, Jan 28
?>
