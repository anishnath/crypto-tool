
# Sequence from 0 to 5
print("range(6):")
for i in range(6):
    print(i, end=" ")
print("\n")

# Sequence from 2 to 6
print("range(2, 6):")
for i in range(2, 6):
    print(i, end=" ")
print("\n")

# Sequence with step 3
print("range(2, 30, 3):")
for i in range(2, 30, 3):
    print(i, end=" ")
print("\n")

# Negative step
print("range(10, 0, -2):")
for i in range(10, 0, -2):
    print(i, end=" ")
print("\n")
