
# Tuple to list
tup = (1, 2, 3)
x = list(tup)
print(f"Tuple {tup} to list: {x}")

# String to list
s = "Python"
y = list(s)
print(f"String '{s}' to list: {y}")

# Set to list
st = {1, 2, 3}
z = list(st)
print(f"Set {st} to list: {z}")

# Dictionary keys to list
d = {'a': 1, 'b': 2}
k = list(d)
print(f"Dict {d} keys to list: {k}")
