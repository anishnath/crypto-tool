
# Integer to float
x = float(3)
print(f"float(3) = {x}")

# String to float
y = float("3.14")
print(f"float('3.14') = {y}")

# Scientific notation string
z = float("1.2e3")
print(f"float('1.2e3') = {z}")

# Infinity
inf = float("inf")
print(f"float('inf') = {inf}")

# Negative infinity
neg_inf = float("-inf")
print(f"float('-inf') = {neg_inf}")
