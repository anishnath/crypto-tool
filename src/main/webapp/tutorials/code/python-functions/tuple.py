
# List to tuple
lst = [1, 2, 3]
x = tuple(lst)
print(f"List {lst} to tuple: {x}")

# String to tuple
s = "Python"
y = tuple(s)
print(f"String '{s}' to tuple: {y}")

# Set to tuple
st = {1, 2, 3}
z = tuple(st)
print(f"Set {st} to tuple: {z}")

# Range to tuple
r = range(5)
t = tuple(r)
print(f"Range {r} to tuple: {t}")
