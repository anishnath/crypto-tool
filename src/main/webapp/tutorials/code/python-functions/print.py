# Python print() function example

# Basic printing
print("Hello, World!")

# Printing multiple items
name = "Alice"
age = 30
print("Name:", name, "Age:", age)

# Specifying a separator
print("Apple", "Banana", "Cherry", sep=", ")

# Specifying an ending characters
print("This line ends with a space", end=" ")
print("and allows this to be on the same line.")

# Formatting output
pi = 3.14159
print(f"Value of pi: {pi:.2f}")
