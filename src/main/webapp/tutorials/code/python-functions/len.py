
# String length
str_val = "Hello World"
print(f"Length of '{str_val}':", len(str_val))

# List length
list_val = [1, 2, 3, 4, 5]
print(f"Length of {list_val}:", len(list_val))

# Dictionary length
dict_val = {'a': 1, 'b': 2, 'c': 3}
print(f"Length of {dict_val}:", len(dict_val))

# Range length
range_val = range(1, 10)
print(f"Length of range(1, 10):", len(range_val))
